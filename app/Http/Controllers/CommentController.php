    {
        $cmt = new Comment();
        $cmt->comment = $request->comment;
        $cmt->user_id = $request->user_id;
        $cmt->post_id = $request->post_id;
        $cmt->save();
        return redirect(route('post.index'))->with('status','Commented');
    }

